int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
	{
		if (nb == -2147483648)
			return (0);
		else
			nb *= -1;
	}
	while (i <= nb / 3)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
